public class BooleanExamples {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);
        System.out.println(a&&a);
        System.out.println(a||a);
        int x = 10;
        int y = 12;
        boolean b = x > y;
        System.out.println(b);
        boolean c = x == y;
        System.out.println(c);
    }
}
